#Comparison operators
# "==" operator

a=15
b=15
op1= (a==b)
print("The Result of '{}'=='{}' is '{}'".format(a,b,op1))
print(f"The Result of '{a}'=='{b}' is '{op1}'")

# "!=" operator

op2 = (a!=b)
print("The Result of '{}'!='{}' is '{}'".format(a,b,op2))
print(f"The Result of '{a}'=='{b}' is '{op2}'")

# ">" operator

op3 = (a>b)
print("The Result of '{}'>'{}' is '{}'".format(a,b,op3))
print(f"The Result of '{a}'>'{b}' is '{op3}'")

# "<" operator

op4 = (a<b)
print("The Result of '{}'<'{}' is '{}'".format(a,b,op4))
print(f"The Result of '{a}'<'{b}' is '{op4}'")

# ">=" operator

op5 = (a>=b)
print("The Result of '{}'>='{}' is '{}'".format(a,b,op5))
print(f"The Result of '{a}'>='{b}' is '{op5}'")

# "<=" operator

op6 = (a<=b)
print("The Result of '{}'<='{}' is '{}'".format(a,b,op6))
print(f"The Result of '{a}'<='{b}' is '{op6}'")